# --- Utility Functions (formerly in utils.py) ---

def sigmoid(x):
    """Numerically stable sigmoid function."""
    return 1 / (1 + np.exp(-np.clip(x, -500, 500)))

def clip_weights(W, limit):
    """Clips the weight matrix values between -limit and +limit."""
    return np.clip(W, -limit, limit)

def generate_training_data(words, word2idx, window_size):
    """Generates (target_index, context_index) pairs using the sliding window."""
    training_data = []
    vocab_indices = [word2idx.get(w) for w in words if w in word2idx]

    for i, target_idx in enumerate(vocab_indices):
        if target_idx is None:
            continue

        start = max(0, i - window_size)
        end = min(len(vocab_indices), i + window_size + 1)

        for j in range(start, end):
            if i != j:
                context_idx = vocab_indices[j]
                if context_idx is not None:
                    training_data.append((target_idx, context_idx))

    return training_data

def get_negative_sampling_table(word_counts, vocab_size, idx2word):
    """Creates a large pre-computed table for efficient negative sampling based on frequency^0.75."""
    word_freqs = np.array([word_counts[idx2word[i]] for i in range(vocab_size)])
    P_neg = np.power(word_freqs, 0.75)
    P_neg /= np.sum(P_neg)

    NEG_SAMPLING_TABLE_SIZE = 100000
    table = np.random.choice(vocab_size, size=NEG_SAMPLING_TABLE_SIZE, p=P_neg)
    return table

