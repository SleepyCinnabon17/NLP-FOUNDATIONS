"""# CELL 4: Semantic Similarity Test

This block evaluates the semantic quality of the trained embeddings by calculating the Cosine Similarity between word vectors.

"""

def get_embedding(word):
    """Retrieves the trained vector for a word."""
    if word in word2idx:
        return W1[word2idx[word]]
    return None

def cosine_similarity(v1, v2):
    """Calculates the cosine of the angle between two vectors."""
    # Check for NaN values and zero vectors for stability
    if np.isnan(v1).any() or np.isnan(v2).any():
        return -1.0
    norm_v1 = np.linalg.norm(v1)
    norm_v2 = np.linalg.norm(v2)
    if norm_v1 == 0 or norm_v2 == 0:
        return -1.0
    return np.dot(v1, v2) / (norm_v1 * norm_v2)

def find_most_similar(word, top_n=5):
    """Finds the top N words with the highest cosine similarity to the query word."""
    target_vec = get_embedding(word)
    if target_vec is None or np.isnan(target_vec).any():
        return f"'{word}' not found or corrupted."

    similarities = {}
    for i in range(vocab_size):
        if idx2word[i] != word:
            sim = cosine_similarity(target_vec, W1[i])
            if sim > -1.0:
                 similarities[idx2word[i]] = sim

    sorted_similarities = sorted(similarities.items(), key=lambda item: item[1], reverse=True)
    return sorted_similarities[:top_n]

