word2idx = {w: i for i, w in enumerate(vocab)}
idx2word = {i: w for w, i in word2idx.items()}
vocab_size = len(vocab)
print(f"Vocabulary size after filtering: {vocab_size}")

# Step 5: Generate training data and Negative Sampling Table
training_data = generate_training_data(words, word2idx, WINDOW_SIZE)
print(f"Training pairs (Target, Context): {len(training_data)}")
neg_sampling_table = get_negative_sampling_table(word_counts, vocab_size, idx2word)
TABLE_SIZE = len(neg_sampling_table)
table_idx = 0

# Step 6: Initialize weights
INIT_RANGE = 0.5
W1 = np.random.uniform(-INIT_RANGE, INIT_RANGE, (vocab_size, EMBEDDING_DIM)) / math.sqrt(EMBEDDING_DIM)
W2 = np.random.uniform(-INIT_RANGE, INIT_RANGE, (EMBEDDING_DIM, vocab_size)) / math.sqrt(EMBEDDING_DIM)

print("Weights initialized successfully.")

