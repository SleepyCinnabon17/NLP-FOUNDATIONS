# CELL 3: Skip-Gram Training Loop

# This cell executes the training process using the Skip-Gram model with Negative Sampling.
# The low learning rate (0.005) ensures numerical stability.

print(f"\n🚀 Training Word2Vec model (W_SIZE={WINDOW_SIZE}, NEG_SAMPLES={NEGATIVE_SAMPLES})...")

def get_next_negative_sample(target_idx):
    """Draws a negative sample from the precomputed table, skipping the target word."""
    global table_idx
    while True:
        negative_idx = neg_sampling_table[table_idx]
        table_idx = (table_idx + 1) % TABLE_SIZE
        if negative_idx != target_idx:
            return negative_idx

for epoch in range(NUM_EPOCHS):
    total_loss = 0
    random.shuffle(training_data)

    for target_idx, context_idx in training_data:
        v_in = W1[target_idx, :] # Target vector (from W1)

        # 1. Prepare positive and negative samples
        indices_to_update = [(context_idx, 1)] # Positive sample (label 1)
        for _ in range(NEGATIVE_SAMPLES):
            neg_idx = get_next_negative_sample(target_idx)
            if neg_idx != context_idx:
                 indices_to_update.append((neg_idx, 0)) # Negative sample (label 0)

        grad_W1_sum = np.zeros(EMBEDDING_DIM) # Accumulate gradient for the input vector

        # 2. Update W2 and accumulate gradient for W1
        for out_idx, label in indices_to_update:
            v_out = W2[:, out_idx] # Output vector (from W2)
            score = np.dot(v_in, v_out)
            prediction = sigmoid(score)
            error = prediction - label

            # Loss accumulation (Binary Cross-Entropy)
            total_loss += - (label * np.log(prediction + 1e-7) + (1 - label) * np.log(1 - prediction + 1e-7))

            # Backpropagation updates
            dW2_col = error * v_in
            W2[:, out_idx] -= LEARNING_RATE * dW2_col
            grad_W1_sum += error * v_out # Accumulate for input vector update

        # 3. Update W1 (Input Vector)
        W1[target_idx, :] -= LEARNING_RATE * grad_W1_sum

    # Apply weight clipping at the end of each epoch
    W1 = clip_weights(W1, CLIP_LIMIT)
    W2 = clip_weights(W2, CLIP_LIMIT)

    avg_loss = total_loss / (len(training_data) * (1 + NEGATIVE_SAMPLES))
    print(f"Epoch {epoch+1}/{NUM_EPOCHS}, Average Loss: {avg_loss:.4f}")

# Save the final embeddings
