# Step 3: Stream and Preprocess (CRITICAL FIX FOR RAM)
print("Preprocessing text (streaming line-by-line)...")
words = []
token_count = 0
try:
    with open(sample_path, "r", encoding="utf-8", errors="ignore") as f:
        # Iterate line by line instead of reading the whole file at once (f.read())
        for line in f:
            if token_count >= MAX_TOKENS:
                break

            # Simple tokenization and lowercasing
            tokens = line.strip().lower().split()

            # Efficiently extend the list and track the total count
            for token in tokens:
                if token_count < MAX_TOKENS:
                    words.append(token)
                    token_count += 1
                else:
                    break

except FileNotFoundError:
    print(f"FATAL ERROR: Data file not found at {sample_path}. Check extraction steps.")
    # Exit or raise error if running as a script

