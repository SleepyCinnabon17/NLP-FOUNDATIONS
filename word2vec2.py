# --- Hyperparameters ---
WINDOW_SIZE = 3
EMBEDDING_DIM = 100
# CRITICAL FIX for Stability: Lowering the learning rate
LEARNING_RATE = 0.005
NUM_EPOCHS = 2
MIN_COUNT = 50
# Using 5M tokens to accommodate a longer run time for better quality
MAX_TOKENS = 2_000_000
NEGATIVE_SAMPLES = 5
CLIP_LIMIT = 5.0

